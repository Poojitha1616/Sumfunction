
public class SumFunction {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 20;
        int sum = calculateSum(n1, n2);
        System.out.println("The sum of " + n1 + " and " + n2 + " is: " + sum);
    }

    public static int calculateSum(int num1, int num2) {
        return num1 + num2;
    }
}
